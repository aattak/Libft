char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	h;

	i = 0;
	h = (char)c;
	while (s[i] != '\0' && s[i] != h)
		i++;
	if (s[i] == h)
		return ((char *)s + i);
	return (NULL);
}
